word = " my name is lakhan"
print(len(word))
print(word.startswith("my")) # to check if the string starts with that characters, returns True or False only
print(word.endswith("khwab")) # to check if the string ends with that characters, returns True or False only
print(word.capitalize()) # first character of string to upper case
print(word.upper()) # all letters upper case 
print(word.title()) # make start letter of each word capital
print(word.strip()) # removes starting and trailing whitespaces
print(word.count("ak")) # counts occurences of that string/letter
print(word.find("name")) # finds the index of start of that string
print(word.replace("lakhan","Abhishek")) # replaces all occurences of string with requires one
print(word*12) # repetition is done by *, same for lists and tuples
print("my" in word ) # Used to check if a word is in a string, returns true or false












